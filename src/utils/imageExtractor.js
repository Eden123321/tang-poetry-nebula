  const calculateFameScore = (poem) => {
    let score = 0;

    // 1. 诗句数量（绝句/律诗 vs 古体）- 适当加权
    if (poem.content && poem.content.length === 4) {
      score += 5; // 绝句
    } else if (poem.content && poem.content.length === 8) {
      score += 8; // 律诗
    } else if (poem.content && poem.content.length > 8) {
      score += 3; // 古体
    }

    // 2. 知名诗句（最高权重，诗句本身耳熟能详）
    if (poem.content) {
      const contentStr = poem.content.join('');
      let lineMatchCount = 0;
      for (const line of famousLines) {
        if (contentStr.includes(line)) {
          lineMatchCount++;
        }
      }
      score += lineMatchCount * 50; // 每匹配一句知名诗句加50分
    }

    // 3. 诗题知名度
    const titleLower = poem.title.toLowerCase();
    if (famousTitles.some(t => titleLower.includes(t))) {
      score += 30;
    }

    // 4. 作者评分
    const authorScore = famousAuthors[poem.author] || 50;
    score += authorScore;

    return score;
  };

  // 按知名程度降序排序
  poems.sort((a, b) => {
    return calculateFameScore(b) - calculateFameScore(a);
  });

