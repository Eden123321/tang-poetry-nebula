// 转换函数 - 优先处理多字符词组，再处理单字符
function toSimplified(text) {
  if (!text) return text;

  let result = text;

  // 先处理多字符词组（按长度降序排列，优先匹配更长的词组）
  const multiCharEntries = Object.entries(t2s).filter(([k]) => k.length > 1);
  multiCharEntries.sort((a, b) => b[0].length - a[0].length);

  for (const [trad, simp] of multiCharEntries) {
    if (result.includes(trad)) {
      result = result.split(trad).join(simp);
    }
  }

  // 再处理单字符
  result = result.split('').map(c => t2s[c] || c).join('');

  return result;
}

