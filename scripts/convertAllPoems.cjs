// 读取所有唐诗
const allPoems = [];
let totalPoems = 0;

for (const file of tangFiles) {
  const filePath = path.join(dataDir, file);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

  for (const poem of data) {
    // 转换标题和作者
    const simplifiedTitle = toSimplified(poem.title || '');
    const simplifiedAuthor = toSimplified(poem.author || '');

    // 转换诗歌内容（paragraphs -> content）
    const simplifiedContent = (poem.paragraphs || []).map(line => toSimplified(line));

    const newPoem = {
      id: allPoems.length + 1,
      title: simplifiedTitle,
      author: simplifiedAuthor,
      dynasty: 'Tang',
      content: simplifiedContent,
    };

    allPoems.push(newPoem);
  }

  totalPoems += data.length;
  console.log(`${file}: ${data.length} poems, total so far: ${allPoems.length}`);
}

